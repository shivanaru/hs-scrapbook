// js/app.js
// Main entry point Ś wires together auth, carousel, wall, filters, submit, lightbox
// ?????????????????????????????????????????????????????????

import { initAuth }         from "./auth.js";
import { initCarousel, prependCarouselSlide } from "./carousel.js";
import { fetchPosts }       from "./posts.js";
import { renderCard }       from "./render.js";
import { initSubmit }       from "./submit.js";

// ?????????????????????????????????????????????????????????
//  Toast
// ?????????????????????????????????????????????????????????
function showToast(msg, variant = "") {
  let container = document.querySelector(".toast-container");
  if (!container) {
    container = document.createElement("div");
    container.className = "toast-container";
    document.body.appendChild(container);
  }
  const toast = document.createElement("div");
  toast.className = `toast${variant ? " toast--" + variant : ""}`;
  toast.textContent = msg;
  container.appendChild(toast);
  setTimeout(() => toast.remove(), 3200);
}

// ?????????????????????????????????????????????????????????
//  Wall state
// ?????????????????????????????????????????????????????????
let currentFilter = "all";
let lastDoc       = null;
let isLoading     = false;
let hasMore       = true;
let allPosts      = [];

let gridEl  = null;
let loaderEl = null;
let emptyEl  = null;

// ?????????????????????????????????????????????????????????
//  Load more button
// ?????????????????????????????????????????????????????????
let loadMoreBtn = null;

function ensureLoadMoreBtn() {
  if (loadMoreBtn) return;
  loadMoreBtn = document.createElement("button");
  loadMoreBtn.id        = "loadMoreBtn";
  loadMoreBtn.className = "btn btn--ghost";
  loadMoreBtn.textContent = "Load more memoriesģ";
  loadMoreBtn.addEventListener("click", () => loadMorePosts());
  // Append after the grid
  gridEl.insertAdjacentElement("afterend", loadMoreBtn);
}

function updateLoadMoreBtn() {
  if (!loadMoreBtn) return;
  loadMoreBtn.hidden = !hasMore;
}

// ?????????????????????????????????????????????????????????
//  Render helpers
// ?????????????????????????????????????????????????????????
function appendCards(posts) {
  posts.forEach((post) => {
    const card = renderCard(post, false);
    gridEl.appendChild(card);
  });
}

function clearGrid() {
  // Remove all children except the loader
  Array.from(gridEl.children).forEach((child) => {
    if (child !== loaderEl) child.remove();
  });
}

function showLoader(on) {
  loaderEl.hidden = !on;
}

function showEmpty(on) {
  emptyEl.hidden = !on;
}

// ?????????????????????????????????????????????????????????
//  Fetch & render initial wall
// ?????????????????????????????????????????????????????????
async function loadInitialPosts() {
  if (isLoading) return;
  isLoading = true;
  showLoader(true);
  showEmpty(false);

  try {
    const result = await fetchPosts({ type: currentFilter });
    allPosts     = result.posts;
    lastDoc      = result.lastDoc;
    hasMore      = result.lastDoc !== null;

    clearGrid();
    if (allPosts.length === 0) {
      showEmpty(true);
    } else {
      appendCards(allPosts);
    }
    ensureLoadMoreBtn();
    updateLoadMoreBtn();
  } catch (err) {
    console.error("Failed to load posts:", err);
    showToast("Couldn't load posts. Check your connection.", "error");
  } finally {
    isLoading = false;
    showLoader(false);
  }
}

// ?????????????????????????????????????????????????????????
//  Load more (pagination)
// ?????????????????????????????????????????????????????????
async function loadMorePosts() {
  if (isLoading || !hasMore) return;
  isLoading = true;
  if (loadMoreBtn) {
    loadMoreBtn.disabled    = true;
    loadMoreBtn.textContent = "Loadingģ";
  }

  try {
    const result = await fetchPosts({ type: currentFilter, lastDoc });
    lastDoc  = result.lastDoc;
    hasMore  = result.lastDoc !== null;
    allPosts = [...allPosts, ...result.posts];
    appendCards(result.posts);
    updateLoadMoreBtn();
  } catch (err) {
    console.error("Load more error:", err);
    showToast("Couldn't load more posts.", "error");
  } finally {
    isLoading = false;
    if (loadMoreBtn) {
      loadMoreBtn.disabled    = false;
      loadMoreBtn.textContent = "Load more memoriesģ";
    }
  }
}

// ?????????????????????????????????????????????????????????
//  Filters
// ?????????????????????????????????????????????????????????
function initFilters() {
  const filterBtns = document.querySelectorAll(".filter-btn");
  filterBtns.forEach((btn) => {
    btn.addEventListener("click", async () => {
      const filter = btn.dataset.filter;
      if (filter === currentFilter) return;

      filterBtns.forEach((b) => b.classList.remove("filter-btn--active"));
      btn.classList.add("filter-btn--active");

      currentFilter = filter;
      lastDoc       = null;
      hasMore       = true;
      allPosts      = [];

      await loadInitialPosts();
    });
  });
}

// ?????????????????????????????????????????????????????????
//  New post handler (called after successful submit)
// ?????????????????????????????????????????????????????????
function handleNewPost(post) {
  // If current filter matches, prepend card to grid
  const matchesFilter = currentFilter === "all" || currentFilter === post.type;
  if (matchesFilter) {
    // Hide empty state if shown
    showEmpty(false);

    const card = renderCard(post, false);
    // Insert after loader (first real card position)
    if (loaderEl.nextSibling) {
      gridEl.insertBefore(card, loaderEl.nextSibling);
    } else {
      gridEl.appendChild(card);
    }
    allPosts.unshift(post);
  }

  // If featured, prepend to carousel
  if (post.featured) {
    prependCarouselSlide(post);
  }
}

// ?????????????????????????????????????????????????????????
//  Lightbox close
// ?????????????????????????????????????????????????????????
function initLightbox() {
  const lb       = document.getElementById("lightbox");
  const closeBtn = document.getElementById("lightboxClose");

  function closeLightbox() {
    lb.classList.remove("lightbox--visible");
    lb.setAttribute("aria-hidden", "true");
    document.body.style.overflow = "";
    document.getElementById("lightboxImg").src = "";
  }

  closeBtn.addEventListener("click", closeLightbox);
  lb.addEventListener("click", (e) => { if (e.target === lb) closeLightbox(); });
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && !lb.hidden) closeLightbox();
  });
}

// ?????????????????????????????????????????????????????????
//  FAB (floating action button) Ś inject into DOM
// ?????????????????????????????????????????????????????????
function injectFAB() {
  const fab = document.createElement("button");
  fab.className   = "fab";
  fab.textContent = "+";
  fab.setAttribute("aria-label", "Add a memory");
  fab.setAttribute("aria-haspopup", "dialog");
  document.body.appendChild(fab);
  // The submit module will pick up .fab via querySelector
}

// ?????????????????????????????????????????????????????????
//  Boot sequence Ś called after auth passes
// ?????????????????????????????????????????????????????????
async function boot() {
const appEl = document.getElementById("app");
appEl.hidden = false;

gridEl   = document.getElementById("postsGrid");
loaderEl = document.getElementById("gridLoader");
emptyEl  = document.getElementById("emptyState");

  injectFAB();
  initLightbox();
  initFilters();

  // Run carousel + wall in parallel for faster load
  await Promise.allSettled([
    initCarousel(),
    loadInitialPosts(),
  ]);

  initSubmit({ onPostAdded: handleNewPost });
}

// ?????????????????????????????????????????????????????????
//  Entry
// ?????????????????????????????????????????????????????????
try {
  initAuth({ onSuccess: boot });
} catch (e) {
  console.error("[app] top-level crash:", e);
  document.getElementById("gateError").textContent = "App error: " + e.message;
}
